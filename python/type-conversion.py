# int, string -> float
print(float(3)) # 3.0
print(float('3.2')) # 3.2


# int, float -> string
print(str(85)) # '85'
print(str(-9.5)) # '-9.5'


# float -> int
print(int(8.14)) # 8
print(round(8.14)) # 8
print(round(8.5)) # 8 银行家圆整


# str -> int, float
print(int('5')) # 5
print(float('5.1')) # 5.1


# str, int, float -> bool
print(bool('1')) # True
print(bool(1)) # True
print(bool('1.2')) # True