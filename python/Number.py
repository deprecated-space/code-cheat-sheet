# `//` 运算符，除法，得到整数
print(2 // 4) # 0
print(7 // 4) # 1
print(-7 // 4) # -2


# `**` 运算符，乘方
print(2 ** 3) # 8


print(abs(-7)) # 7
print(max(1, 2, 3)) # 3
print(min(1, 2, 3)) # 1
print(pow(2, 3)) # 8


# 数学函数
import math

print(math.ceil(7.1)) # 8
print(math.floor(7.1)) # 7
print(math.fabs(7)) # 7.0
print(math.sqrt(2)) # 1.4142135623730951
print(math.factorial(5)) # 123


